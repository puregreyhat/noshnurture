export function translateIngredient(ingredient: string, language: string): string {
  const lowerIngredient = ingredient.toLowerCase().trim();
  
  // Try exact match first
  if (INGREDIENT_TRANSLATIONS[language]?.[lowerIngredient]) {
    return INGREDIENT_TRANSLATIONS[language][lowerIngredient];
  }
  
  // Try partial match (for phrases like "1 onion" -> "onion")
  const words = lowerIngredient.split(' ');
  for (const word of words) {
    if (INGREDIENT_TRANSLATIONS[language]?.[word]) {
      return INGREDIENT_TRANSLATIONS[language][word];
    }
  }
  
  // Return original if no translation found
  return ingredient;
}
